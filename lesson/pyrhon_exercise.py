# Calculate the sequence
for i in range(2, n):
    next_num = fibonacci_seq[i-1] + fibonacci_seq[i-2]
    fibonacci_seq.append(next_num)

# Display the sequence
print("Fibonacci sequence:")
for num in fibonacci_seq:
    print(num, end=" ")

#recursive function

def fibonacci(n):
    if n == 0:
        return 0
    elif n == 1:
        return 1
    else:
        return fibonacci(n-1) + fibonacci(n-2)

# Ask user for input
n = int(input("Enter the number of the Fibonacci sequence: "))

# Calculate and display the sequence
