"""# (Q3) เขียนโปรแกรมเพื่อแสดงรูปแบบตามที่กำหนด โดยรับ input เป็นขนาดของ pattern เป็นจำนวนคี่ และแสดงผลเป็น pattern ที่สัมพันธ์กับขนาดที่ได้รับ """

n = int(input("Enter an odd number: "))

# check if n is odd
if n % 2 == 0:
    print("Please enter an odd number.")
else:
    # loop through the rows
    for i in range(n):
        # loop through the columns
        for j in range(n):
            # check if i and j are equal or if their sum is n-1
            if i == j or i + j == n - 1:
                print("*", end="")
            else:
                print("-", end="")
        # print a newline character at the end of each row
        print()

