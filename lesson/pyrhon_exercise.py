num_a = int(input("Enter the number of A characters: "))
num_b = int(input("Enter the number of B characters: "))

# initialize variables to keep track of the number of consecutive characters
consecutive_a = 0
consecutive_b = 0

# loop through each character in the output
for i in range(num_a + num_b):
    # check if we should output an A or a B
    if num_a > num_b:
        # output an A if we have more As than Bs
        if consecutive_a < 2:
            print("A", end="")
            consecutive_a += 1
            consecutive_b = 0
            num_a -= 1
        else:
            print("B", end="")
            consecutive_a = 0
            consecutive_b += 1
            num_b -= 1
    else:
        # output a B if we have more Bs than As
        if consecutive_b < 2:
            print("B", end="")
            consecutive_b += 1
            consecutive_a = 0
            num_b -= 1
        else:
            print("A", end="")
            consecutive_b = 0
            consecutive_a += 1
            num_a -= 1

# print a newline character at the end of the output
